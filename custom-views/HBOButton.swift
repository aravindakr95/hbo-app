import Foundation
import UIKit

class HBOButton: UIButton {
    
    override init(frame: CGRect) {
        super.init(frame: frame)
        setupButton()
    }
    
    required init?(coder aDecoder: NSCoder) {
        super.init(coder: aDecoder)
        setupButton()
    }
    
    private func setupButton() {
        clipsToBounds = true
        layer.cornerRadius  = CGFloat(5)
    }
}

